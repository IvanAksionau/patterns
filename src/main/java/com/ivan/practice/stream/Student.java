package com.ivan.practice.stream;

public class Student {

    private String name;
    private String age;
    private String mark;

    public Student(String mark) {
        this.mark = mark;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getMark() {
        return mark;
    }
}
